package main.java.prob5;

public class Point {
    private int xCoordinate;
    private int yCoordinate;

    public Point(int x, int y) {
        this.xCoordinate = x;
        this.yCoordinate = y;
    }

    public int getXCoordinate() {
        return xCoordinate;
    }
    public int getYCoordinate(){
        return yCoordinate;
    }

}
